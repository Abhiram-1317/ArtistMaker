// Standalone queue smoke test вЂ” verifies Bull + Redis job processing end-to-end
// Run: npx tsx src/test-queue.ts
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

import {
  renderQueue,
  addRenderJob,
  type RenderJobData,
  type GenerateShotResult,
} from "./queues/renderQueue.js";
import { queueEvents, attachQueueEvents } from "./queues/queueEvents.js";
import { startRenderWorker } from "./workers/renderWorker.js";

async function runTest(): Promise<void> {
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log("  рџ§Є Queue Smoke Test вЂ” Bull + Redis");
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

  // в”Ђв”Ђ Step 1: Wire up events + worker в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log("1пёЏвѓЈ  Attaching queue events and starting worker...");
  attachQueueEvents(renderQueue);
  startRenderWorker(2);

  // Track events for verification
  const events: string[] = [];

  // Listen via the event bus (which uses global: Bull events internally)
  queueEvents.on("job:progress", (evt) => {
    events.push(`progress:${evt.jobId}:${evt.progress}%`);
    console.log(
      `   рџ“Љ Progress вЂ” Job ${evt.jobId} (${evt.jobType}): ${evt.progress}%`,
    );
  });

  queueEvents.on("job:completed", (evt) => {
    events.push(`completed:${evt.jobId}`);
    console.log(
      `   вњ… Completed вЂ” Job ${evt.jobId} (${evt.jobType})`,
    );
  });

  queueEvents.on("job:failed", (evt) => {
    events.push(`failed:${evt.jobId}`);
    console.log(
      `   вќЊ Failed вЂ” Job ${evt.jobId} (${evt.jobType}): ${evt.error}`,
    );
  });

  console.log("   Worker ready.\n");

  // в”Ђв”Ђ Step 2: Enqueue a generate-shot job в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log("2пёЏвѓЈ  Enqueueing a generate-shot job...");
  const shotJob = await addRenderJob(
    {
      type: "generate-shot",
      shotId: "test-shot-001",
      sceneId: "test-scene-001",
      projectId: "test-project-001",
      config: {
        quality: "draft",
        resolution: { width: 1280, height: 720 },
        fps: 24,
        shotType: "medium",
        cameraMovement: "static",
        duration: 3,
        prompt: "A cinematic establishing shot of a futuristic city",
      },
    },
    "pro",
  );
  console.log(`   Job enqueued: ID=${shotJob.id}, type=${shotJob.data.type}\n`);

  // в”Ђв”Ђ Step 3: Enqueue a generate-audio job в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log("3пёЏвѓЈ  Enqueueing a generate-audio job...");
  const audioJob = await addRenderJob(
    {
      type: "generate-audio",
      sceneId: "test-scene-001",
      projectId: "test-project-001",
      audioType: "dialogue",
      config: {
        duration: 5,
        description: "Ambient city soundscape",
        characterName: "Narrator",
        dialogueText: "In the year 2077...",
      },
    },
    "creator",
  );
  console.log(`   Job enqueued: ID=${audioJob.id}, type=${audioJob.data.type}\n`);

  // в”Ђв”Ђ Step 4: Wait for jobs to complete в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log("4пёЏвѓЈ  Waiting for jobs to process...\n");

  const [shotResult, audioResult] = await Promise.all([
    shotJob.finished() as Promise<GenerateShotResult>,
    audioJob.finished(),
  ]);

  console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log("  рџ“‹ Results");
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");

  console.log("  Shot Result:", JSON.stringify(shotResult, null, 2));
  console.log("  Audio Result:", JSON.stringify(audioResult, null, 2));

  // в”Ђв”Ђ Step 5: Check queue stats в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  const [waiting, active, completed, failed] = await Promise.all([
    renderQueue.getWaitingCount(),
    renderQueue.getActiveCount(),
    renderQueue.getCompletedCount(),
    renderQueue.getFailedCount(),
  ]);

  console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log("  рџ“€ Queue Stats");
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");
  console.log(`  Waiting:   ${waiting}`);
  console.log(`  Active:    ${active}`);
  console.log(`  Completed: ${completed}`);
  console.log(`  Failed:    ${failed}`);

  // в”Ђв”Ђ Step 6: Verify в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  const progressEvents = events.filter((e) => e.startsWith("progress:"));
  const completedEvents = events.filter((e) => e.startsWith("completed:"));
  const failedEvents = events.filter((e) => e.startsWith("failed:"));

  console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log("  вњ… Verification");
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ\n");
  console.log(`  Progress events fired: ${progressEvents.length}`);
  console.log(`  Completed events fired: ${completedEvents.length}`);
  console.log(`  Failed events fired:   ${failedEvents.length}`);

  const allPassed =
    completed >= 2 &&
    failed === 0 &&
    completedEvents.length >= 2 &&
    shotResult.videoUrl.includes("cdn.genesis.ai") &&
    shotResult.thumbnailUrl.includes("cdn.genesis.ai");

  console.log(
    `\n  ${allPassed ? "рџЋ‰ ALL CHECKS PASSED" : "вљ пёЏ  SOME CHECKS FAILED"} вЂ” Queue system is ${allPassed ? "working correctly" : "not fully functional"}!`,
  );

  // в”Ђв”Ђ Cleanup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log("\n  Cleaning up...");
  await renderQueue.obliterate({ force: true });
  await renderQueue.close();
  console.log("  Done.\n");
  process.exit(allPassed ? 0 : 1);
}

runTest().catch((err) => {
  console.error("вќЊ Test failed with error:", err);
  process.exit(1);
});
